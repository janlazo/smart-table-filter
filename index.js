import {compose} from 'smart-table-operators';
import pointer from 'smart-table-json-pointer';

function typeExpression (type) {
  switch (type) {
    case 'boolean':
      return Boolean;
    case 'number':
      return Number;
    case 'date':
      return (val) => new Date(val);
    default:
      return compose(String, (val) => val.toLowerCase());
  }
}

const operators = {
  includes(value){
    return (input) => input.includes(value);
  },
  is(value){
    return (input) => Object.is(value, input);
  },
  lt(value){
    return (input) => input < value;
  },
  gt(value){
    return (input) => input > value;
  },
  lte(value){
    return (input) => input <= value;
  },
  gte(value){
    return (input) => input >= value;
  },
  equals(value){
    return (input) => value == input;
  }
};

const every = fns => (...args) => fns.every(fn => fn(...args));

export function predicate ({value = '', operator = 'includes', type = ''}) {
  const typeIt = typeExpression(type);
  const operateOnTyped = compose(typeIt, operators[operator]);
  const predicateFunc = operateOnTyped(value);
  return compose(typeIt, predicateFunc);
}

export default function filter (filter) {
  const validPathList = Object.keys(filter).filter(path => Array.isArray(filter[path]) && filter[path].length);
  const funcList = validPathList.map(path => {
    const getter = pointer(path).get;
    const clauses = filter[path].map(predicate);
    return compose(getter, every(clauses));
  });
  const filterPredicate = every(funcList);

  return (array) => array.filter(filterPredicate);
}